/// Design system icon size tokens for Very Yummy Coffee.
///
/// Provides common icon sizes used across the UI.
abstract class SharedIconSize {
  /// Small icon size: 8px
  static const double small = 8;

  /// Medium icon size: 16px
  static const double medium = 16;

  /// Large icon size: 24px
  static const double large = 24;

  /// Large icon size with selected state: 28px
  static const double largeSelected = 28;
}
